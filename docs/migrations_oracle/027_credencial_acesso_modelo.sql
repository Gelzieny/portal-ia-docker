CREATE TABLE CIA_CREDENCIAL_ACESSO_MODELO (
    ID_CREDENCIAL_ACESSO NUMBER(10) NOT NULL,
    ID_SOLICITACAO_ACESSO NUMBER(10) NOT NULL,
    DESC_ENDPOINT_BASE_URL VARCHAR2(500 CHAR) NOT NULL,
    DESC_CHAVE_ACESSO_CRIPTO VARCHAR2(255 CHAR) NOT NULL,
    DESC_SEGREDO_ACESSO_CRIPTO VARCHAR2(255 CHAR) NOT NULL,
    JSON_CABECALHO_PUBLICO CLOB DEFAULT '{}' NOT NULL,
    DESC_OBSERVACAO_USO CLOB DEFAULT '' NOT NULL,
    INDI_ATIVO VARCHAR2(1 CHAR) DEFAULT 'S' NOT NULL,
    CODF_USUARIO_CRIACAO NUMBER(10) NOT NULL,
    CODG_USUARIO_ATUALIZACAO NUMBER(10),
    DATA_CRIACAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    DATA_ATUALIZACAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,

    CONSTRAINT PK_CIA_CREDENCIAL_ACESSO
        PRIMARY KEY (ID_CREDENCIAL_ACESSO),

    CONSTRAINT UK_CIA_CREDENCIAL_SOLIC
        UNIQUE (ID_SOLICITACAO_ACESSO),

    CONSTRAINT FK_CIA_CREDENCIAL_SOLIC
        FOREIGN KEY (ID_SOLICITACAO_ACESSO)
        REFERENCES CIA_SOLICITACAO_ACESSO_MODELO (ID_SOLICITACAO_ACESSO)
        ON DELETE CASCADE,

    CONSTRAINT FK_CIA_CREDENCIAL_USER_CRIACAO
        FOREIGN KEY (CODF_USUARIO_CRIACAO)
        REFERENCES USUARIO_SISTEMA (CODG_USUARIO)
        ON DELETE RESTRICT,

    CONSTRAINT FK_CIA_CREDENCIAL_USER_ATUAL
        FOREIGN KEY (CODG_USUARIO_ATUALIZACAO)
        REFERENCES USUARIO_SISTEMA (CODG_USUARIO)
        ON DELETE SET NULL,

    CONSTRAINT CK_CIA_CREDENCIAL_ATIVO
        CHECK (
            INDI_ATIVO IN ('S', 'N')
        )
)


CREATE SEQUENCE SEQ_CIA_CREDENCIAL_ACESSO
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE


CREATE OR REPLACE TRIGGER TRG_BI_CIA_CREDENCIAL
BEFORE INSERT ON CIA_CREDENCIAL_ACESSO_MODELO
FOR EACH ROW
BEGIN
    IF :NEW.ID_CREDENCIAL_ACESSO IS NULL THEN
        SELECT SEQ_CIA_CREDENCIAL_ACESSO.NEXTVAL
          INTO :NEW.ID_CREDENCIAL_ACESSO
          FROM DUAL
    END IF
END

CREATE OR REPLACE TRIGGER TRG_BU_CIA_CREDENCIAL
BEFORE UPDATE ON CIA_CREDENCIAL_ACESSO_MODELO
FOR EACH ROW
BEGIN
    :NEW.DATA_ATUALIZACAO := CURRENT_TIMESTAMP
END

CREATE INDEX IDX_CIA_CREDENCIAL_SOLIC
    ON CIA_CREDENCIAL_ACESSO_MODELO (ID_SOLICITACAO_ACESSO)

CREATE INDEX IDX_CIA_CREDENCIAL_USUARIO
    ON CIA_CREDENCIAL_ACESSO_MODELO (
        CODF_USUARIO_CRIACAO,
        CODG_USUARIO_ATUALIZACAO
    )


