CREATE TABLE CIA_BANCO_QUESTAO (
    ID_BANCO_QUESTAO INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    ID_METRICA INTEGER,
    INFO_PERGUNTA TEXT NOT NULL,
    INFO_GABARITO TEXT NOT NULL,

    CONSTRAINT PK_CIA_BANCO_QUESTAO PRIMARY KEY (ID_BANCO_QUESTAO),

    CONSTRAINT FK_CIA_BANCO_QUESTAO_METRICA FOREIGN KEY (ID_METRICA)
        REFERENCES CIA_METRICA (ID_METRICA) ON DELETE SET NULL
)
;

CREATE UNIQUE INDEX UK_CIA_BANCO_QUESTAO ON CIA_BANCO_QUESTAO (ID_BANCO_QUESTAO, ID_METRICA)
;

COMMENT ON TABLE CIA_BANCO_QUESTAO IS
'Tabela responsável pelo armazenamento das questões vinculadas às métricas de avaliação da plataforma.';

COMMENT ON COLUMN CIA_BANCO_QUESTAO.ID_BANCO_QUESTAO IS
'Identificador único da questão cadastrada no banco de questões.';

COMMENT ON COLUMN CIA_BANCO_QUESTAO.ID_METRICA IS
'Identificador da métrica associada à questão cadastrada.';

COMMENT ON COLUMN CIA_BANCO_QUESTAO.INFO_PERGUNTA IS
'Conteúdo estruturado da pergunta armazenado em formato JSON.';

COMMENT ON COLUMN CIA_BANCO_QUESTAO.INFO_GABARITO IS
'Conteúdo estruturado do gabarito da questão armazenado em formato JSON.';
