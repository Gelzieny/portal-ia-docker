CREATE TABLE CIA_RECURSO_MCP (
    ID_RECURSO_MCP INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    ID_SERVIDOR_MCP INTEGER NOT NULL,

    NOME_RECURSO VARCHAR(200) NOT NULL,
    URI_TEMPLATE VARCHAR(500) NOT NULL,
    DESC_RECURSO TEXT DEFAULT '' NOT NULL,

    TIPO_MIME VARCHAR(100),

    NUMR_ORDEM INTEGER DEFAULT 0 NOT NULL,

    CONSTRAINT PK_CIA_RECURSO_MCP
        PRIMARY KEY (ID_RECURSO_MCP),

    CONSTRAINT FK_CIA_RECURSO_MCP_SERV
        FOREIGN KEY (ID_SERVIDOR_MCP)
        REFERENCES CIA_SERVIDOR_MCP (ID_SERVIDOR_MCP)
        ON DELETE CASCADE
)
;

CREATE INDEX IDX_CIA_RECURSO_MCP_SERV
    ON CIA_RECURSO_MCP (ID_SERVIDOR_MCP)
;

COMMENT ON TABLE CIA_RECURSO_MCP IS
'Tabela responsável pelo cadastro dos recursos disponibilizados pelos servidores MCP da plataforma.';

COMMENT ON COLUMN CIA_RECURSO_MCP.ID_RECURSO_MCP IS
'Identificador único do recurso MCP cadastrado.';

COMMENT ON COLUMN CIA_RECURSO_MCP.ID_SERVIDOR_MCP IS
'Identificador do servidor MCP associado ao recurso.';

COMMENT ON COLUMN CIA_RECURSO_MCP.NOME_RECURSO IS
'Nome de identificação do recurso disponibilizado pelo servidor MCP.';

COMMENT ON COLUMN CIA_RECURSO_MCP.URI_TEMPLATE IS
'Modelo de URI utilizado para acesso dinâmico ao recurso MCP.';

COMMENT ON COLUMN CIA_RECURSO_MCP.DESC_RECURSO IS
'Descrição detalhada do recurso disponibilizado pelo servidor MCP.';

COMMENT ON COLUMN CIA_RECURSO_MCP.TIPO_MIME IS
'Tipo MIME associado ao conteúdo retornado pelo recurso MCP.';

COMMENT ON COLUMN CIA_RECURSO_MCP.NUMR_ORDEM IS
'Ordem de exibição do recurso MCP nas interfaces da plataforma.';
