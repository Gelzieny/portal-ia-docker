CREATE TABLE CIA_LOG_AUDITORIA (
    ID_LOG_AUDITORIA INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    ID_USUARIO INTEGER,
    ACAO_AUDITORIA VARCHAR(50) NOT NULL,
    NOME_ENTIDADE VARCHAR(100) NOT NULL,
    ID_ENTIDADE INTEGER,
    INFO_METADADO TEXT,
    INFO_ENDERECO_IP VARCHAR(45),
    DATA_CRIACAO TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP NOT NULL,
    INFO_USER_AGENT TEXT,

    CONSTRAINT PK_CIA_LOG_AUDITORIA PRIMARY KEY (ID_LOG_AUDITORIA),

    CONSTRAINT FK_CIA_LOG_AUDITORIA_USUARIO FOREIGN KEY (ID_USUARIO)
        REFERENCES CIA_USUARIO (ID_USUARIO) ON DELETE SET NULL
)
;

CREATE INDEX IDX_CIA_LOG_AUD_ENTIDADE ON CIA_LOG_AUDITORIA (NOME_ENTIDADE, ID_ENTIDADE);
;

CREATE INDEX IDX_CIA_LOG_AUD_USUARIO ON CIA_LOG_AUDITORIA (ID_USUARIO, DATA_CRIACAO);
;

COMMENT ON TABLE CIA_LOG_AUDITORIA IS
'Tabela responsável pelo armazenamento dos registros de auditoria das operações realizadas na aplicação.';

COMMENT ON COLUMN CIA_LOG_AUDITORIA.ID_LOG_AUDITORIA IS
'Identificador único do registro de auditoria.';

COMMENT ON COLUMN CIA_LOG_AUDITORIA.ID_USUARIO IS
'Identificador do usuário responsável pela operação auditada.';

COMMENT ON COLUMN CIA_LOG_AUDITORIA.ACAO_AUDITORIA IS
'Ação executada pelo usuário na aplicação.';

COMMENT ON COLUMN CIA_LOG_AUDITORIA.NOME_ENTIDADE IS
'Nome da entidade ou recurso afetado pela operação auditada.';

COMMENT ON COLUMN CIA_LOG_AUDITORIA.ID_ENTIDADE IS
'Identificador do registro relacionado à operação auditada.';

COMMENT ON COLUMN CIA_LOG_AUDITORIA.INFO_METADADO IS
'Informações complementares e metadados relacionados ao evento de auditoria armazenados em formato JSON.';

COMMENT ON COLUMN CIA_LOG_AUDITORIA.INFO_ENDERECO_IP IS
'Endereço IP de origem da requisição realizada pelo usuário.';

COMMENT ON COLUMN CIA_LOG_AUDITORIA.DATA_CRIACAO IS
'Data e hora de criação do registro de auditoria.';

COMMENT ON COLUMN CIA_LOG_AUDITORIA.INFO_USER_AGENT IS
'Informações do agente de navegação utilizado pelo usuário durante a operação.';
