'Data e hora da última atualização do registro da credencial de acesso.';

CREATE OR REPLACE FUNCTION trg_bu_cia_credencial_fn()
RETURNS TRIGGER AS $$
BEGIN
  NEW.data_atualizacao = CURRENT_TIMESTAMP;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_bu_cia_credencial
  BEFORE UPDATE ON cia_credencial_acesso_modelo
  FOR EACH ROW EXECUTE FUNCTION trg_bu_cia_credencial_fn();
