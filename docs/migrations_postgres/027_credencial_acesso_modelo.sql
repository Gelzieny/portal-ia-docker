CREATE TABLE CIA_CREDENCIAL_ACESSO_MODELO (
    ID_CREDENCIAL_ACESSO INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    ID_SOLICITACAO_ACESSO INTEGER NOT NULL,
    URL_ENDPOINT_BASE VARCHAR(500) NOT NULL,
    CHAVE_ACESSO_CRIPTO TEXT NOT NULL,
    SEGREDO_ACESSO_CRIPTO TEXT NOT NULL,
    INFO_CABECALHO_PUBLICO TEXT DEFAULT '{}' NOT NULL,
    DESC_OBSERVACAO_USO TEXT DEFAULT '' NOT NULL,
    INDI_ATIVO VARCHAR(1) DEFAULT 'S' NOT NULL,
    ID_USUARIO_CRIACAO INTEGER NOT NULL,
    ID_USUARIO_ATUALIZACAO INTEGER,
    DATA_CRIACAO TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP NOT NULL,
    DATA_ATUALIZACAO TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP NOT NULL,

    CONSTRAINT PK_CIA_CREDENCIAL_ACESSO
        PRIMARY KEY (ID_CREDENCIAL_ACESSO),

    CONSTRAINT UK_CIA_CREDENCIAL_SOLIC
        UNIQUE (ID_SOLICITACAO_ACESSO),

    CONSTRAINT FK_CIA_CREDENCIAL_SOLIC
        FOREIGN KEY (ID_SOLICITACAO_ACESSO)
        REFERENCES CIA_SOLICITACAO_ACESSO_MODELO (ID_SOLICITACAO_ACESSO)
        ON DELETE CASCADE,

    CONSTRAINT FK_CIA_CREDENCIAL_USER_CRIACAO
        FOREIGN KEY (ID_USUARIO_CRIACAO)
        REFERENCES CIA_USUARIO (ID_USUARIO)
        ON DELETE RESTRICT,

    CONSTRAINT FK_CIA_CREDENCIAL_USER_ATUAL
        FOREIGN KEY (ID_USUARIO_ATUALIZACAO)
        REFERENCES CIA_USUARIO (ID_USUARIO)
        ON DELETE SET NULL,

    CONSTRAINT CK_CIA_CREDENCIAL_ATIVO
        CHECK (
            INDI_ATIVO IN ('S', 'N')
        )
)
;

CREATE INDEX IDX_CIA_CREDENCIAL_SOLIC
    ON CIA_CREDENCIAL_ACESSO_MODELO (ID_SOLICITACAO_ACESSO)
;

CREATE INDEX IDX_CIA_CREDENCIAL_USUARIO
    ON CIA_CREDENCIAL_ACESSO_MODELO (
        ID_USUARIO_CRIACAO,
        ID_USUARIO_ATUALIZACAO
    )
;

