CREATE TABLE CIA_TOPICO_IDEIA (
    ID_TOPICO_IDEIA INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    NOME_TOPICO VARCHAR(120) NOT NULL,
    SLUG_TOPICO VARCHAR(140) NOT NULL,
    COR_TOPICO VARCHAR(20) NOT NULL,
    NOME_ICONE VARCHAR(80),
    DESC_TOPICO TEXT,
    NUMR_ORDEM INTEGER DEFAULT 0 NOT NULL,
    INDI_ATIVO VARCHAR(1) DEFAULT 'S' NOT NULL,
    DATA_CRIACAO TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP NOT NULL,
    DATA_ATUALIZACAO TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP NOT NULL,

    CONSTRAINT PK_CIA_TOPICO_IDEIA PRIMARY KEY (ID_TOPICO_IDEIA),

    CONSTRAINT UK_CIA_TOPICO_IDEA_NOME UNIQUE (NOME_TOPICO),

    CONSTRAINT UK_CIA_TOPICO_IDEA_SLUG UNIQUE (SLUG_TOPICO),

    CONSTRAINT CK_CIA_TOPICO_IDEA_ATIVO CHECK (INDI_ATIVO IN ('S', 'N'))
)
;

CREATE INDEX IDX_CIA_TOPICO_IDEA_ATIVO
    ON CIA_TOPICO_IDEIA (
        INDI_ATIVO,
        NUMR_ORDEM,
        NOME_TOPICO
    )
;

COMMENT ON TABLE CIA_TOPICO_IDEIA IS
'Tabela responsável pelo cadastro dos tópicos utilizados para categorização das ideias da plataforma.';

COMMENT ON COLUMN CIA_TOPICO_IDEIA.ID_TOPICO_IDEIA IS
'Identificador único do tópico de ideia.';

COMMENT ON COLUMN CIA_TOPICO_IDEIA.NOME_TOPICO IS
'Nome do tópico utilizado para categorização das ideias.';

COMMENT ON COLUMN CIA_TOPICO_IDEIA.SLUG_TOPICO IS
'Identificador textual único utilizado em URLs e navegação dos tópicos.';

COMMENT ON COLUMN CIA_TOPICO_IDEIA.COR_TOPICO IS
'Código ou identificação visual da cor associada ao tópico.';

COMMENT ON COLUMN CIA_TOPICO_IDEIA.NOME_ICONE IS
'Nome do ícone associado visualmente ao tópico.';

COMMENT ON COLUMN CIA_TOPICO_IDEIA.DESC_TOPICO IS
'Descrição detalhada do tópico de ideia.';

COMMENT ON COLUMN CIA_TOPICO_IDEIA.NUMR_ORDEM IS
'Ordem de exibição do tópico nas interfaces da plataforma.';

COMMENT ON COLUMN CIA_TOPICO_IDEIA.INDI_ATIVO IS
'Indica se o tópico está ativo na plataforma. S = Sim, N = Não.';

COMMENT ON COLUMN CIA_TOPICO_IDEIA.DATA_CRIACAO IS
'Data e hora de criação do registro do tópico.';

COMMENT ON COLUMN CIA_TOPICO_IDEIA.DATA_ATUALIZACAO IS
'Data e hora da última atualização do registro do tópico.';

CREATE OR REPLACE FUNCTION trg_bu_cia_topico_ideia_fn()
RETURNS TRIGGER AS $$
BEGIN
  NEW.data_atualizacao = CURRENT_TIMESTAMP;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_bu_cia_topico_ideia
  BEFORE UPDATE ON cia_topico_ideia
  FOR EACH ROW EXECUTE FUNCTION trg_bu_cia_topico_ideia_fn();
