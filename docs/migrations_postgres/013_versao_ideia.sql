CREATE TABLE CIA_VERSAO_IDEIA (
    ID_VERSAO_IDEIA INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    NOME_VERSAO VARCHAR(120) NOT NULL,
    DESC_VERSAO TEXT,
    INFO_PREVISAO VARCHAR(80),
    NUMR_ORDEM INTEGER DEFAULT 0 NOT NULL,
    INDI_ATIVO VARCHAR(1) DEFAULT 'S' NOT NULL,
    ID_USUARIO_CRIACAO INTEGER,
    DATA_CRIACAO TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP NOT NULL,
    DATA_ATUALIZACAO TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP NOT NULL,

    CONSTRAINT PK_CIA_VERSAO_IDEIA PRIMARY KEY (ID_VERSAO_IDEIA),

    CONSTRAINT UK_CIA_VERSAO_IDEA_NOME UNIQUE (NOME_VERSAO),

    CONSTRAINT FK_CIA_VERSAO_IDEA_USUARIO FOREIGN KEY (ID_USUARIO_CRIACAO)
        REFERENCES CIA_USUARIO (ID_USUARIO) ON DELETE SET NULL,

    CONSTRAINT CK_CIA_VERSAO_IDEA_ATIVO CHECK (INDI_ATIVO IN ('S', 'N'))
)
;

CREATE INDEX IDX_CIA_VERSAO_IDEA_ATIVO
    ON CIA_VERSAO_IDEIA (
        INDI_ATIVO,
        NUMR_ORDEM,
        NOME_VERSAO
    )
;

COMMENT ON TABLE CIA_VERSAO_IDEIA IS
'Tabela responsável pelo cadastro das versões utilizadas para organização e planejamento das ideias da plataforma.';

COMMENT ON COLUMN CIA_VERSAO_IDEIA.ID_VERSAO_IDEIA IS
'Identificador único da versão da ideia.';

COMMENT ON COLUMN CIA_VERSAO_IDEIA.NOME_VERSAO IS
'Nome da versão utilizada para agrupamento das ideias.';

COMMENT ON COLUMN CIA_VERSAO_IDEIA.DESC_VERSAO IS
'Descrição detalhada da versão cadastrada.';

COMMENT ON COLUMN CIA_VERSAO_IDEIA.INFO_PREVISAO IS
'Informação de previsão ou período estimado relacionado à versão da ideia.';

COMMENT ON COLUMN CIA_VERSAO_IDEIA.NUMR_ORDEM IS
'Ordem de exibição da versão nas interfaces da plataforma.';

COMMENT ON COLUMN CIA_VERSAO_IDEIA.INDI_ATIVO IS
'Indica se a versão está ativa na plataforma. S = Sim, N = Não.';

COMMENT ON COLUMN CIA_VERSAO_IDEIA.ID_USUARIO_CRIACAO IS
'Identificador do usuário responsável pela criação da versão da ideia.';

COMMENT ON COLUMN CIA_VERSAO_IDEIA.DATA_CRIACAO IS
'Data e hora de criação do registro da versão.';

COMMENT ON COLUMN CIA_VERSAO_IDEIA.DATA_ATUALIZACAO IS
'Data e hora da última atualização do registro da versão.';

CREATE OR REPLACE FUNCTION trg_bu_cia_versao_ideia_fn()
RETURNS TRIGGER AS $$
BEGIN
  NEW.data_atualizacao = CURRENT_TIMESTAMP;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_bu_cia_versao_ideia
  BEFORE UPDATE ON cia_versao_ideia
  FOR EACH ROW EXECUTE FUNCTION trg_bu_cia_versao_ideia_fn();
