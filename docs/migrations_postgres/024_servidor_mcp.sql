-- STAT_SERVIDOR:
--     DP = Disponível
--     BT = Beta
--     EX = Experimental
--     DC = Descontinuado

CREATE TABLE CIA_SERVIDOR_MCP (
    ID_SERVIDOR_MCP INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,

    NOME_SERVIDOR VARCHAR(200) NOT NULL,
    SLUG_SERVIDOR VARCHAR(200) NOT NULL,

    DESC_RESUMIDA VARCHAR(300) DEFAULT '' NOT NULL,
    DESC_SERVIDOR TEXT DEFAULT '' NOT NULL,

    ID_CATEGORIA_MCP INTEGER,

    STAT_SERVIDOR VARCHAR(2) DEFAULT 'EX' NOT NULL,

    INDI_VERIFICADO VARCHAR(1) DEFAULT 'N' NOT NULL,
    INDI_DESTAQUE VARCHAR(1) DEFAULT 'N' NOT NULL,
    INDI_OFICIAL VARCHAR(1) DEFAULT 'N' NOT NULL,

    URL_REPOSITORIO VARCHAR(500),
    URL_DOCUMENTACAO VARCHAR(500),
    URL_HOMEPAGE VARCHAR(500),

    NUMR_VERSAO VARCHAR(50),
    NOME_LICENCA VARCHAR(100),

    INFO_MODELOS_COMPATIVEIS TEXT DEFAULT '[]' NOT NULL,

    VALR_MEDIA_AVALIACAO NUMERIC(3,2) DEFAULT 0 NOT NULL,
    QTDE_AVALIACAO INTEGER DEFAULT 0 NOT NULL,
    QTDE_INSTALACAO INTEGER DEFAULT 0 NOT NULL,

    NOME_AUTOR VARCHAR(200),
    NOME_ORGANIZACAO_AUTOR VARCHAR(200),

    ID_USUARIO_SUBMISSAO INTEGER,

    TAG TEXT DEFAULT '[]' NOT NULL,

    NUMR_ORDEM INTEGER DEFAULT 0 NOT NULL,

    INDI_ATIVO VARCHAR(1) DEFAULT 'S' NOT NULL,

    DATA_CRIACAO TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP NOT NULL,
    DATA_ATUALIZACAO TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP NOT NULL,

    CONSTRAINT PK_CIA_SERVIDOR_MCP
        PRIMARY KEY (ID_SERVIDOR_MCP),

    CONSTRAINT UK_CIA_SERVIDOR_MCP_SLUG
        UNIQUE (SLUG_SERVIDOR),

    CONSTRAINT FK_CIA_SERVIDOR_MCP_CAT
        FOREIGN KEY (ID_CATEGORIA_MCP)
        REFERENCES CIA_CATEGORIA_MCP (ID_CATEGORIA_MCP)
        ON DELETE SET NULL,

    CONSTRAINT FK_CIA_SERVIDOR_MCP_USER
        FOREIGN KEY (ID_USUARIO_SUBMISSAO)
        REFERENCES CIA_USUARIO (ID_USUARIO)
        ON DELETE SET NULL,

    CONSTRAINT CK_CIA_SERVIDOR_STATUS
        CHECK (
            STAT_SERVIDOR IN (
                'DP',
                'BT',
                'EX',
                'DC'
            )
        ),

    CONSTRAINT CK_CIA_SERVIDOR_VERIFICADO
        CHECK (
            INDI_VERIFICADO IN ('S', 'N')
        ),

    CONSTRAINT CK_CIA_SERVIDOR_DESTAQUE
        CHECK (
            INDI_DESTAQUE IN ('S', 'N')
        ),

    CONSTRAINT CK_CIA_SERVIDOR_OFICIAL
        CHECK (
            INDI_OFICIAL IN ('S', 'N')
        ),

    CONSTRAINT CK_CIA_SERVIDOR_ATIVO
        CHECK (
            INDI_ATIVO IN ('S', 'N')
        )
)
;

CREATE INDEX IDX_CIA_SERVIDOR_MCP_CAT ON CIA_SERVIDOR_MCP (ID_CATEGORIA_MCP);
;

CREATE INDEX IDX_CIA_SERVIDOR_MCP_DEST ON CIA_SERVIDOR_MCP (INDI_DESTAQUE);
;

CREATE INDEX IDX_CIA_SERVIDOR_MCP_SLUG ON CIA_SERVIDOR_MCP (SLUG_SERVIDOR);
;

CREATE INDEX IDX_CIA_SERVIDOR_MCP_STATUS  ON CIA_SERVIDOR_MCP (STAT_SERVIDOR);
;

