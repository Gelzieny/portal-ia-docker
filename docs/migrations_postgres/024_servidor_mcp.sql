CREATE OR REPLACE FUNCTION trg_bu_cia_servidor_mcp_fn()
RETURNS TRIGGER AS $$
BEGIN
  NEW.data_atualizacao = CURRENT_TIMESTAMP;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_bu_cia_servidor_mcp
  BEFORE UPDATE ON cia_servidor_mcp
  FOR EACH ROW EXECUTE FUNCTION trg_bu_cia_servidor_mcp_fn();
