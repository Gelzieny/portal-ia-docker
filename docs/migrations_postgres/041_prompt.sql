CREATE OR REPLACE FUNCTION trg_bu_cia_prompt_fn()
RETURNS TRIGGER AS $$
BEGIN
  NEW.data_atualizacao = CURRENT_TIMESTAMP;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_bu_cia_prompt
  BEFORE UPDATE ON cia_prompt
  FOR EACH ROW EXECUTE FUNCTION trg_bu_cia_prompt_fn();
