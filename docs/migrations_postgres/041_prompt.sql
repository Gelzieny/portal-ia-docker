-- STAT_PUBLICACAO:
--     'RS': Rascunho,
--     'PV': Privado,
--     'AG': Aguardando Aprovação,
--     'PB': Publicado,
--     'ER': Em Revisão,
--     'AR': Arquivado

-- NIVEL_DIFICULDADE:
--     'IN': Iniciante,
--     'IT': Intermediário,
--     'AV': Avançado

CREATE TABLE CIA_PROMPT (
    ID_PROMPT INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    TITULO_PROMPT VARCHAR(300) NOT NULL,
    DESC_PROMPT TEXT DEFAULT '' NOT NULL,
    DESC_CONTEUDO TEXT NOT NULL,
    ID_CATEGORIA_PROMPT INTEGER,
    ID_MODELO INTEGER,
    TAG TEXT DEFAULT '[]' NOT NULL,
    NIVEL_DIFICULDADE VARCHAR(2) DEFAULT 'IN' NOT NULL,
    QTDE_USO INTEGER DEFAULT 0 NOT NULL,
    INFO_VARIAVEL TEXT DEFAULT '[]' NOT NULL,
    ID_USUARIO_AUTOR INTEGER,
    INDI_PUBLICO VARCHAR(1) DEFAULT 'S' NOT NULL,
    INDI_ATIVO VARCHAR(1) DEFAULT 'S' NOT NULL,
    DATA_CRIACAO TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP NOT NULL,
    DATA_ATUALIZACAO TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP NOT NULL,
    VALR_MEDIA_AVALIACAO NUMERIC(3,2) DEFAULT 0 NOT NULL,
    QTDE_AVALIACAO INTEGER DEFAULT 0 NOT NULL,
    STAT_PUBLICACAO VARCHAR(2) DEFAULT 'PV' NOT NULL,
    TIPO_ORIGEM VARCHAR(2) DEFAULT 'OF' NOT NULL,
    DATA_SUBMISSAO TIMESTAMPTZ,
    ID_USUARIO_REVISAO INTEGER,
    DATA_REVISAO TIMESTAMPTZ,
    DESC_OBSERVACAO_REVISAO TEXT,
    NOME_AUTOR_ORIGINAL VARCHAR(200),
    QTDE_DENUNCIA INTEGER DEFAULT 0 NOT NULL,
    NUMR_VERSAO INTEGER DEFAULT 1 NOT NULL,
    DESC_OBSERVACAO_SUBMISSAO TEXT,

    CONSTRAINT PK_CIA_PROMPT PRIMARY KEY (ID_PROMPT),

    CONSTRAINT FK_CIA_PROMPT_AUTOR
        FOREIGN KEY (ID_USUARIO_AUTOR)
        REFERENCES CIA_USUARIO (ID_USUARIO)
        ON DELETE SET NULL,

    CONSTRAINT FK_CIA_PROMPT_CATEGORIA
        FOREIGN KEY (ID_CATEGORIA_PROMPT)
        REFERENCES CIA_CATEGORIA_PROMPT (ID_CATEGORIA_PROMPT)
        ON DELETE SET NULL,

    CONSTRAINT FK_CIA_PROMPT_MODELO
        FOREIGN KEY (ID_MODELO)
        REFERENCES CIA_MODELO (ID_MODELO)
        ON DELETE SET NULL,

    CONSTRAINT FK_CIA_PROMPT_REVISAO
        FOREIGN KEY (ID_USUARIO_REVISAO)
        REFERENCES CIA_USUARIO (ID_USUARIO)
        ON DELETE SET NULL,

    CONSTRAINT CK_CIA_PROMPT_DIFICULDADE
        CHECK (
            NIVEL_DIFICULDADE IN (
                'IN',
                'IT',
                'AV'
            )
        ),

    CONSTRAINT CK_CIA_PROMPT_PUBLICO
        CHECK (
            INDI_PUBLICO IN ('S', 'N')
        ),

    CONSTRAINT CK_CIA_PROMPT_ATIVO
        CHECK (
            INDI_ATIVO IN ('S', 'N')
        ),

    CONSTRAINT CK_CIA_PROMPT_PUBLICACAO
        CHECK (
            STAT_PUBLICACAO IN (
                'RS',
                'PV',
                'AG',
                'PB',
                'ER',
                'AR'
            )
        ),

    CONSTRAINT CK_CIA_PROMPT_ORIGEM
        CHECK (
            TIPO_ORIGEM IN (
                'OF',
                'US'
            )
        )
)
;

CREATE INDEX IDX_CIA_PROMPT_AUTOR_ORIGEM
    ON CIA_PROMPT (
        ID_USUARIO_AUTOR,
        TIPO_ORIGEM
    )
;

CREATE INDEX IDX_CIA_PROMPT_AGUARDANDO
    ON CIA_PROMPT (
        STAT_PUBLICACAO,
        DATA_SUBMISSAO
    )
;

CREATE INDEX IDX_CIA_PROMPT_CATEGORIA
    ON CIA_PROMPT (ID_CATEGORIA_PROMPT)
;

CREATE INDEX IDX_CIA_PROMPT_DIFICULDADE
    ON CIA_PROMPT (NIVEL_DIFICULDADE)
;

CREATE INDEX IDX_CIA_PROMPT_REVISAO
    ON CIA_PROMPT (
        STAT_PUBLICACAO,
        QTDE_DENUNCIA
    )
;

CREATE INDEX IDX_CIA_PROMPT_PUBLICACAO ON CIA_PROMPT (STAT_PUBLICACAO);
;

CREATE INDEX IDX_CIA_PROMPT_ORIGEM ON CIA_PROMPT (TIPO_ORIGEM);
;

