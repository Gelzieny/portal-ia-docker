CREATE OR REPLACE FUNCTION trg_bu_cia_solic_acesso_fn()
RETURNS TRIGGER AS $$
BEGIN
  NEW.data_atualizacao = CURRENT_TIMESTAMP;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_bu_cia_solic_acesso
  BEFORE UPDATE ON cia_solicitacao_acesso_modelo
  FOR EACH ROW EXECUTE FUNCTION trg_bu_cia_solic_acesso_fn();
