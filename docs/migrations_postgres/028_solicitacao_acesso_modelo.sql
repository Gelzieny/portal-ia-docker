-- STAT_SOLICITACAO:
--     PE = Pendente,
--     AP = Aprovado,
--     NG = Negado,
--     RS = Revogação Solicitada,
--     RV = Revogado,
--     CA = Cancelado

CREATE TABLE CIA_SOLICITACAO_ACESSO_MODELO (
    ID_SOLICITACAO_ACESSO INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,

    ID_MODELO INTEGER NOT NULL,
    ID_USUARIO INTEGER NOT NULL,

    NOME_APLICACAO VARCHAR(200) NOT NULL,

    STAT_SOLICITACAO VARCHAR(2) DEFAULT 'PE' NOT NULL,

    DESC_JUSTIFICATIVA TEXT NOT NULL,
    DESC_USO_PRETENDIDO TEXT,

    INFO_CONTEXTO_SOLICITACAO TEXT DEFAULT '{}' NOT NULL,

    ID_USUARIO_REVISAO INTEGER,
    DATA_REVISAO TIMESTAMPTZ,
    DESC_OBSERVACAO_REVISAO TEXT,

    DATA_CRIACAO TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP NOT NULL,
    DATA_ATUALIZACAO TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP NOT NULL,

    CONSTRAINT PK_CIA_SOLICITACAO_ACESSO
        PRIMARY KEY (ID_SOLICITACAO_ACESSO),

    CONSTRAINT FK_CIA_SOLIC_ACESSO_MODELO
        FOREIGN KEY (ID_MODELO)
        REFERENCES CIA_MODELO (ID_MODELO)
        ON DELETE CASCADE,

    CONSTRAINT FK_CIA_SOLIC_ACESSO_USUARIO
        FOREIGN KEY (ID_USUARIO)
        REFERENCES CIA_USUARIO (ID_USUARIO)
        ON DELETE CASCADE,

    CONSTRAINT FK_CIA_SOLIC_ACESSO_REVISAO
        FOREIGN KEY (ID_USUARIO_REVISAO)
        REFERENCES CIA_USUARIO (ID_USUARIO)
        ON DELETE SET NULL,

    CONSTRAINT CK_CIA_SOLIC_STATUS
        CHECK (
            STAT_SOLICITACAO IN (
                'PE',
                'AP',
                'NG',
                'RS',
                'RV',
                'CA'
            )
        )
)
;

CREATE INDEX IDX_CIA_SOLIC_ACESSO_APLIC
    ON CIA_SOLICITACAO_ACESSO_MODELO (NOME_APLICACAO)
;

CREATE INDEX IDX_CIA_SOLIC_ACESSO_MODELO
    ON CIA_SOLICITACAO_ACESSO_MODELO (ID_MODELO)
;

CREATE INDEX IDX_CIA_SOLIC_ACESSO_STATUS
    ON CIA_SOLICITACAO_ACESSO_MODELO (
        STAT_SOLICITACAO,
        DATA_CRIACAO
    )
;

CREATE INDEX IDX_CIA_SOLIC_ACESSO_USUARIO
    ON CIA_SOLICITACAO_ACESSO_MODELO (ID_USUARIO)
;

CREATE UNIQUE INDEX UK_CIA_SOLIC_ACESSO_ATIVA
    ON CIA_SOLICITACAO_ACESSO_MODELO (
        ID_MODELO,
        ID_USUARIO,
        LOWER(TRIM(NOME_APLICACAO))
    )
;

